const fs = require('fs');
const path = require('path');

const BUILD_DIR = path.join(__dirname, 'build');
const OUTPUT_FILE = path.join(__dirname, 'public', 'cache-manifest.js');
const IGNORE_FILES = [/\.map$/, /sw\.js$/, /precache-manifest\./];

const getFiles = (dir) => {
  let results = [];
  const list = fs.readdirSync(dir);
  console.log(list);
  list.forEach(file => {
    const fullPath = path.join(dir, file);
    const stat = fs.statSync(fullPath);

    if (stat && stat.isDirectory()) {
      results = results.concat(getFiles(fullPath));
    } else {
      if (!IGNORE_FILES.some(regex => regex.test(file))) {
        // Replace backslashes with forward slashes
        results.push(fullPath.replace(BUILD_DIR, '').replace(/\\/g, '/'));
      }
    }
  });

  return results;
};

const files = getFiles(BUILD_DIR);
const jsContent = `// Auto-generated by generate-cache-list.js
const FILES_TO_CACHE = ${JSON.stringify(files, null, 2)};
`;

fs.writeFileSync(OUTPUT_FILE, jsContent);
console.log(`Generated cache list with ${files.length} files in ${OUTPUT_FILE}`);